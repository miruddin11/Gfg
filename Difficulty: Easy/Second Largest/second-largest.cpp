class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        // Code Here
        int f=INT_MIN;
        int s=INT_MIN;
        for(auto &x:arr)
        {
            if(x>f){
                s=f;
                f=x;
            }
            else if(x>s&&x!=f){
                s=x;
            }
        }
        return s==INT_MIN?-1:s;
    }
};

